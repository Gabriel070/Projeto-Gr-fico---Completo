    private void jBCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCadastrarActionPerformed
        String nome = jTNome.getText();
        int matricula = 0;
        try {
            matricula = Integer.parseInt(jTMatricula.getText());
        } catch (NumberFormatException nfe){
            JOptionPane.showMessageDialog(this, "Voc√™ digitou um caracter inv√°lido \n"+ nfe);
            throw new RuntimeException();
        }
        Aluno a = new Aluno(nome, matricula);
        JOptionPane.showMessageDialog(this, a.toString());
    }//GEN-LAST:event_jBCadastrarActionPerformed

    private void jBCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBCalcularActionPerformed
        Aluno a = new Aluno(jTNome.getText(), Integer.parseInt(jTMatricula.getText()));
        TipoAvaliacao tipo = null;
        switch(jCombinacao.getSelectedIndex()){
            case 0:
            {
             tipo = TipoAvaliacao.PROVA;
             break;
            }
            case 1:
            {
             tipo = TipoAvaliacao.SEMINARIO;  
             break;
            }
            case 2:
            {
             tipo = TipoAvaliacao.TRABALHO;
             break;
            }    
        }
        try {
        a.getNotas().add(new Nota(jTDisciplina.getText(), tipo, Float.parseFloat((jTN1.getText()))));
        } catch (NumberFormatException nfe){
            JOptionPane.showMessageDialog(this, "Voc√™ digitou um caracter inv√°lido \n"+ nfe);
            throw new RuntimeException();
        }
        try {
        a.getNotas().add(new Nota(jTDisciplina.getText(), tipo, Float.parseFloat((jTN2.getText()))));
        } catch (NumberFormatException nfe){
            JOptionPane.showMessageDialog(this, "Voc√™ digitou um caracter inv√°lido \n"+ nfe);
            throw new RuntimeException();
        }
        try {
        a.getNotas().add(new Nota(jTDisciplina.getText(), tipo, Float.parseFloat((jTN3.getText()))));
        } catch (NumberFormatException nfe){
            JOptionPane.showMessageDialog(this, "Voc√™ digitou um caracter inv√°lido \n"+ nfe);
            throw new RuntimeException();
        }
        try {
        a.getNotas().add(new Nota(jTDisciplina.getText(), tipo, Float.parseFloat((jTN4.getText()))));
        } catch (NumberFormatException nfe){
            JOptionPane.showMessageDialog(this, "Voc√™ digitou um caracter inv√°lido \n"+ nfe);
            throw new RuntimeException();
        }
        JOptionPane.showMessageDialog(null, a.toString()
                + "\n" + "M√©dia: " + a.calcularMedia());
    }//GEN-LAST:event_jBCalcularActionPerformed

